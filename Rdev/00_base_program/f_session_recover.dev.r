# Function to read and recover a source file
recover_source_file <- function(metadata_file, content_file, out_folder) {
  # Read the metadata
  metadata <- fromJSON(metadata_file)
  
  # Determine the output filename
  filename <- if (!is.null(metadata$path)) {
    basename(metadata$path)
  } else if (!is.null(metadata$properties$tempName)) {
    metadata$properties$tempName
  } else {
    basename(metadata_file)
  }
  
  # Read the contents
  contents <- if (file.exists(content_file) && file.info(content_file)$size > 0) {
    readLines(content_file)
  } else if (!is.null(metadata$contents) && nchar(metadata$contents) > 0) {
    metadata$contents
  } else {
    "No contents found"
  }
  
  # Determine the output path
  target <- file.path(out_folder, filename)
  
  # Ensure no filename conflicts
  if (file.exists(target)) {
    target <- file.path(out_folder, paste0(tools::file_path_sans_ext(filename), "-", basename(metadata_file), ".", tools::file_ext(filename)))
  }
  
  # Write the contents to the target file
  writeLines(contents, con = target)
  
  # Return the path to the recovered file
  return(target)
}

# Iterate over session files to recover metadata and contents
